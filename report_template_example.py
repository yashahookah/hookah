"""
Пример структуры шаблона отчёта
Можно использовать как референс для редактирования
"""

def example_report_structure():
    """
    Пример того, как выглядит структура отчёта
    Все переменные реальные, можно копировать и редактировать
    """
    
    # ============================================
    # 1. ЗАГОЛОВОК
    # Файл: telegram_daily_summary_complete.py, строки 205-207
    # ============================================
    header = """# 📊 Аналитический отчёт за {date_str}

_Сформировано в {time_str} МСК_

---
"""
    
    # ============================================
    # 2. ЧТО ПРОИСХОДИЛО СЕГОДНЯ
    # Файл: telegram_analytics_engine.py, строки 252-309
    # ============================================
    what_happened = """## 📖 Что происходило сегодня

За сегодня в кальянном инфополе появилось **{total_msgs} новых постов** из {channels_count} каналов. 
Самым активным оказался канал **{most_active_channel}**. 
А больше всего внимания аудитории собрал контент от **{most_engaged_channel}**.
В лентах чаще всего мелькали бренды: {top_brands_text}.
Общий настрой обсуждений — **{sentiment_text}**.
"""
    
    # ============================================
    # 3. В ЦИФРАХ
    # Файл: telegram_analytics_engine.py, строки 311-321
    # ============================================
    in_numbers = """## 📊 В цифрах

Всего собрано **{total_msgs} постов**, которые набрали **{total_views:,} просмотров**. 
В среднем каждый пост получил **{avg_views:.0f} просмотров**.

**{media_ratio:.0f}%** контента — это фото и видео, остальное — текстовые посты.
"""
    
    # ============================================
    # 4. КТО И КАК ПИСАЛ
    # Файл: telegram_analytics_engine.py, строки 323-340
    # ============================================
    who_wrote = """## 📱 Кто и как писал

**1. {channel1}** опубликовал {messages1} постов, из них {media1} с фото/видео. 
Средняя вовлечённость — {avg_views1:.0f} просмотров на пост.

**2. {channel2}** опубликовал {messages2} постов. 
Средняя вовлечённость — {avg_views2:.0f} просмотров на пост.
"""
    
    # ============================================
    # 5. О ЧЁМ ГОВОРИЛИ
    # Файл: telegram_analytics_engine.py, строки 342-349
    # ============================================
    what_discussed = """## 💬 О чём говорили

Основные темы дня: **{topic1}** ({count1} раз), **{topic2}** ({count2} раз), **{topic3}** ({count3} раз).
"""
    
    # ============================================
    # 6. ЧТО В ТРЕНДЕ
    # Файл: telegram_analytics_engine.py, строки 351-396
    # ============================================
    what_trending = """## 🎯 Что в тренде

**Бренды дня:** **{brand1}** ({count1}), **{brand2}** ({count2}), **{brand3}** ({count3}).

**Продукты:** **{product1}** ({count1}), **{product2}** ({count2}).

**Вкусы:** **{flavor1}** ({count1}), **{flavor2}** ({count2}).

**События и мероприятия:**

• [{channel1}] {event_text1}
• [{channel2}] {event_text2}

**Бизнес-новости:**

• [{channel1}] {business_text1}
"""
    
    # ============================================
    # 7. НАСТРОЕНИЕ АУДИТОРИИ
    # Файл: telegram_analytics_engine.py, строки 398-411
    # ============================================
    sentiment = """## 😊 Настроение аудитории

Тон обсуждений: **{positive_pct:.0f}%** позитивных, **{neutral_pct:.0f}%** нейтральных, **{negative_pct:.0f}%** негативных.
"""
    
    # ============================================
    # 8. ФОКУС: ADALYA И TANGIERS
    # Файл: telegram_brand_tracker.py, строки 185-227
    # ============================================
    focus_brands = """## 🎯 Фокус: Adalya и Tangiers

**Adalya** упоминалась **{adalya_count} раз** за сегодня.

Упоминания распределены по {channels_count} каналам: **{channel1}** ({count1}), **{channel2}** ({count2}).

**Примеры:**

• [{channel1}, {time1}] {context1}...
• [{channel2}, {time2}] {context2}...

**Tangiers** упоминался **{tangiers_count} раз** за сегодня.

{similar_structure_for_tangiers}
"""
    
    # ============================================
    # 9. ЧТО КОНКРЕТНО ПИСАЛИ БЛОГЕРЫ
    # Файл: telegram_digest_builder.py, строки 212-241
    # ============================================
    digest = """## 📌 Что конкретно писали блогеры

### 🆕 Новинки и релизы ({novelties_count} постов)

1. [{channel1}, {time1}] **{brand1}** представил: {text1}

2. [{channel2}, {time2}] {text2}

### 🎪 Мероприятия и события ({events_count} постов)

1. [{channel1}, {time1}] — **{date1}** {text1}

### 💰 Цены и предложения ({prices_count} постов)

1. [{channel1}, {time1}] **{brand1}** {product1} — **{price1}₽**. {text1}
"""
    
    # ============================================
    # 10. ЧТО МЫ ЗНАЕМ О РЫНКЕ
    # Файл: telegram_learning_system.py, строки 155-186
    # ============================================
    market_insights = """## 💡 Что мы знаем о рынке

**Цены на рынке:** **табак** — обычно **~{tobacco_price:.0f}₽** (от {tobacco_min}₽ до {tobacco_max}₽), 
**кальян** — обычно **~{hookah_price:.0f}₽** (от {hookah_min}₽ до {hookah_max}₽).

**Самые активные бренды:** **{brand1}** ({mentions1}), **{brand2}** ({mentions2}), **{brand3}** ({mentions3}).

**Ближайшие события:**

• [{channel1}] {event_text1}
"""
    
    # ============================================
    # 11. ИТОГО
    # Файл: telegram_daily_summary_complete.py, строки 283-285
    # ============================================
    summary = """---

**Итого:** проанализировано **{total_messages} сообщений** из **{channels_count} каналов** за {date_str}.
"""
    
    # Полный пример отчёта
    full_report = f"""{header}

{what_happened}

---
{in_numbers}

---
{who_wrote}

---
{what_discussed}

---
{what_trending}

---
{sentiment}

---
{focus_brands}

---
{digest}

---
{market_insights}

{summary}
"""
    
    return full_report
